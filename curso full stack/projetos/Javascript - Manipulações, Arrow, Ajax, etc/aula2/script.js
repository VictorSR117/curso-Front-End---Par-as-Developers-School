let nome = "Victor Luiz Lopes da Silva";

console.log("---------------------------------------------------slice---------------------------------------------------");
console.log(nome.slice(10, 15));
console.log(nome.slice(12));
console.log(nome.slice(-5));
console.log(nome.slice(-15, -10));

console.log("-------------------------------------------------substring---------------------------------------------------");

console.log(nome.substring(2, 14));
console.log(nome.substring(8));

console.log("-------------------------------------------------substr-----------------------------------------------------");

console.log(nome.substr(8, 10));
console.log(nome.substr(-20, 11));